    public addAlternative(String name) {
        initComponents();
        
        setTitle("Add a new Alternative to "+name.substring(1)+ " ?");
                
        String url = System.getProperty("user.dir") + "\\datafuzzy.db";
       
        //JOptionPane.showMessageDialog(null, name.substring(1));

        try{
        Conn cn = new Conn(url);
         //cn.c.setAutoCommit(false);
        cn.stmt = cn.c.createStatement();
        String sql = "Select idproblem from problems where name ='"+name.substring(1)+"'";
        ResultSet rs = cn.stmt.executeQuery(sql);
         int idp =0;
        
        while(rs.next()){
               idp= rs.getInt(1);
               idproblem = idp;
               //JOptionPane.showMessageDialog(null,idp);
            }
        
        cn.stmt.close();
        cn.c.close();
        }catch(SQLException err){
                    JOptionPane.showMessageDialog(null, "Data Base Connection Problem (new p 208) "+ err.toString());
                }        
        
        
         

        
    }

